#include <stdlib.h>
#include <stdio.h>
#include <time.h>

/**
* print_alphabet - print all alphabet in lowercase
*
* Description: using the print_alphabet function
* this program print
* "a function that prints the alphabet in lowercase, followed by a new line"
*/

void print_alphabet(void)
{

	char l;

	for (l = 'a'; l <= 'z'; l++)
		putchar('l');

	putchar('\n');
}

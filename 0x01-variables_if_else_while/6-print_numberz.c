#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
* main - print the string in the put function
*
* description: using the main function
* this program prints "All single numbers of base 10 starting from 0"
* Return: 0
*/

int main(void)
{
	int n = 0;

	while (n <= 9)
	{
		putchar(n = 0);
		++n;
	}
	putchar('\n');

	return (0);
}
